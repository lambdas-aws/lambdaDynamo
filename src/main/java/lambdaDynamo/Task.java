package lambdaDynamo;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

import java.util.ArrayList;


@DynamoDBTable(tableName = "task")
public class Task {
    private String id;
    private String title;
    private String description;
    private String status;
    private String assignee;
    private String url;

    private ArrayList<History> historyList;

    public Task(){}

    public Task(String title, String description){
        this.title = title;
        this.description = description;
        this.status = "available";
        this.historyList = new ArrayList<>();
        this.historyList.add(historyCreation(this.status));

        this.assignee = null;
        this.url = null;
    }

    public Task(String title, String description, String assignee){
        this.title = title;
        this.description = description;

        this.status = "assigned";

        this.historyList = new ArrayList<>();
        this.historyList.add(historyCreation(this.status));

        this.assignee = assignee;
        this.url = null;
    }

    public Task(String id, String title, String description, String assignee, ArrayList<History> historyList, String url){
        this.id = id;
        this.title = title;
        this.description = description;

        //history exists, update.
        if (historyList != null){
            this.status = "updated";
            this.historyList = historyList;
            historyList.add(historyCreation(this.status));
        }
        //history is null, make new.
        else{
            this.status = "assigned";
            this.historyList = new ArrayList<>();
            this.historyList.add(historyCreation(this.status));
        }

        this.assignee = assignee;
        this.url = url;
    }

    public Task(String title, String description, String assignee, String url){
        this.id = id;
        this.title = title;
        this.description = description;

        this.status = "assigned";
        this.historyList.add(historyCreation(this.status));

        this.assignee = assignee;
        this.url = url;
    }


    public History historyCreation(String update) {

        History history = new History(update );

        return history;
    }

    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    public String getId() {
        return id;
    }

    @DynamoDBAttribute
    public String getTitle() {
        return this.title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setId(String id) {
        this.id = id;
    }


    @DynamoDBAttribute
    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @DynamoDBAttribute
    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status= status;
    }

    @DynamoDBAttribute
    public String getAssignee() {
        return assignee;
    }

    public void setAssignee(String assignee) {
        this.assignee = assignee;
    }

    @DynamoDBAttribute
    public ArrayList<History> getHistoryList() {
        return historyList;
    }

    public void setHistoryList(ArrayList<History> historyList) {
        this.historyList = historyList;
    }

    @DynamoDBAttribute
    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
